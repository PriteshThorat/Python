print('factorial(5): ',factorial(5))
print('sin(90): ', sin(90))
print('cos(60): ', cos(60))
print('exp(5): ', exp(5))
print('log(16): ', log(16))
print('log(16, 2): ', log(16, 2))
print('pow(144, 0.5): ', pow(144, 0.5))